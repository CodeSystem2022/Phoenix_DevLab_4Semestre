# Sistema decimal
a = 10
print(f"a decimal: {a}")
# Sistema binario
a = 0b1010
print(f"a binario: {a}")
# Sistema octal
a = 0o12
print(f"a octal: {a}")
# Sistema hexadecimal
a = 0xA
print(f"a hexadecimal: {a}")

# Base Decimal
a = int('23', 10)
print(f"a = base decimal: {a}")

# Base binario
a = int('10111', 2)
print(f"a = base binario: {a}")

# Base Octal
a = int('27', 8)
print(f"a = base octal: {a}")

# Base Hexadecimal
a = int('17', 16)
print(f"a = base Hexadecimal: {a}")

# Base 5 sus valores son de 0 a 4
